-- 0051 — هم‌ترازسازی constraint نشانی (slug) با قاعده‌ی واقعی محصول
-- ───────────────────────────────────────────────────────────────────────────
-- باگ: SLUG_PATTERN در src/lib/config.ts و متن SLUG_RULE_TEXT به کاربر می‌گویند
-- «حروف کوچک انگلیسی، عدد، خط تیره و زیرخط» ولی constraint دیتابیس هنوز نسخه‌ی v1 بود و
-- فقط [a-z0-9-] را می‌پذیرفت. نتیجه: کاربر نشانی‌ای مثل dr_matin می‌داد، از همه‌ی
-- اعتبارسنجی‌های اپ رد می‌شد، OTP هم تایید می‌شد، و تازه در insert نهایی با خطای
-- خام Postgres شکست می‌خورد.
--
-- قاعده‌ی نهایی (دقیقا معادل SLUG_PATTERN):
--   • فقط [a-z0-9] و جداکننده‌های _ و -
--   • اول و آخر همیشه حرف یا عدد
--   • دو جداکننده‌ی پشت‌سرهم مجاز نیست (a--b, a-_b)
--   • طول 3 تا 40 نویسه
--
-- NOT VALID عمدی است: قاعده‌ی «دو جداکننده‌ی پشت‌سرهم» از constraint قبلی
-- سخت‌گیرانه‌تر است، پس اگر tenant قدیمی‌ای با slug مثل a--b وجود داشته باشد
-- مهاجرت نباید روی دیتابیس زنده بترکد. constraint برای هر INSERT/UPDATE جدید
-- کامل اعمال می‌شود و ردیف‌های قدیمی دست‌نخورده می‌مانند.
-- برای اعتبارسنجی کامل بعدا (پس از اطمینان از نبود ردیف ناسازگار):
--   alter table public.tenants validate constraint tenants_slug_check;
-- ───────────────────────────────────────────────────────────────────────────

alter table public.tenants drop constraint if exists tenants_slug_check;
alter table public.tenants add constraint tenants_slug_check
  check (
    slug ~ '^[a-z0-9][a-z0-9_-]{1,38}[a-z0-9]$'
    and slug !~ '[_-]{2}'
  ) not valid;
