-- ───────────────────────────────────────────────────────────────────────────
-- 0054 — مکان حضوری per-stage (قرینه‌ی meet_channel)
-- ───────────────────────────────────────────────────────────────────────────
-- additive و idempotent.
--
-- عدم تقارن قدیمی: جلسه‌ی آنلاین ستون meet_channel روی خود مرحله داشت، پس
-- مراجع موقع پرداخت می‌توانست بین واتساپ/میت/تماس یکی را انتخاب کند. ولی جلسه‌ی
-- حضوری هیچ ستون معادلی نداشت — مکان فقط یک‌بار موقع ساخت پرونده (فرم مصاحبه)
-- روی psy_cases.office_location می‌نشست و بعد از آن قابل انتخاب نبود.
--
-- نتیجه در پنل مراجع: حالت آنلاین گزینه‌های روش برگزاری نشان می‌داد، حالت
-- حضوری هیچ گزینه‌ای — حتی وقتی مجموعه چند آدرس تعریف کرده بود.
--
-- این ستون همان قرینه است. خالی‌بودنش یعنی «همان مکان پرونده» (رفتار قبلی).
-- ───────────────────────────────────────────────────────────────────────────

alter table public.psy_stages add column if not exists office_location text;

-- ── حضورنیافتن مراجع در یک مرحله ───────────────────────────────────────────
-- تا امروز فقط held (بله/خیر) داشتیم و «مراجع نیامد» جایی ثبت نمی‌شد؛ متخصص
-- مجبور بود یا دروغ بگوید «برگزار شد» یا مرحله را باز بگذارد و پرونده قفل بماند.
--
-- no_show عمدا کنار held می‌نشیند و جایگزینش نیست: از نظر جریان کار، جلسه‌ای که
-- مراجع نیامده هم مصرف شده و پرونده باید آزاد شود. پس «حاضر نشد» یعنی
-- held=true و no_show=true — تمام منطق موجود که به held نگاه می‌کند دست‌نخورده
-- می‌ماند و فقط برچسب و آمار حضور راست می‌شوند.
alter table public.psy_stages add column if not exists no_show boolean not null default false;
