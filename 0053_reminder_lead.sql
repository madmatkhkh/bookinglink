-- ───────────────────────────────────────────────────────────────────────────
-- 0053 — فاصله‌ی زمانی یادآوری، قابل تنظیم برای هر مجموعه
-- ───────────────────────────────────────────────────────────────────────────
-- additive و idempotent.
--
-- تا امروز یادآوری برای همه یک رفتار ثابت داشت: cron ساعت 18 به وقت ایران
-- اجرا می‌شد و نوبت‌های «فردا» را پیامک می‌کرد. این برای مطبی که نوبت‌های صبح
-- زود دارد خوب است و برای کسی که می‌خواهد دو ساعت قبل یادآوری کند بی‌فایده.
--
-- مدل جدید: به‌جای «شب قبل»، N ساعت قبل از خودِ نوبت. یعنی نوبت ساعت 10 با
-- lead=24 دیروز ساعت 10 یادآوری می‌گیرد و با lead=3 همان روز ساعت 7.
--
-- پیش‌فرض 24 است تا رفتار مجموعه‌های فعلی تقریبا همان قبل بماند (نه دقیقا:
-- قبلا همه ساعت 18 پیام می‌گرفتند، حالا 24 ساعت قبل از نوبت خودشان).
--
-- بازه‌ی مجاز 1 تا 48 ساعت: کمتر از 1 ساعت عملا بی‌فایده است (cron ساعتی
-- است و ممکن است دیر برسد) و بیشتر از 48 ساعت هم پنجره‌ی جست‌وجوی cron را
-- بزرگ‌تر از چیزی می‌کند که پشتیبانی شده.
-- ───────────────────────────────────────────────────────────────────────────

alter table public.tenant_profiles
  add column if not exists reminder_lead_hours integer not null default 24;

alter table public.tenant_profiles drop constraint if exists tenant_profiles_reminder_lead_check;
alter table public.tenant_profiles add constraint tenant_profiles_reminder_lead_check
  check (reminder_lead_hours between 1 and 48);
