-- تم اختصاصی هر متخصص برای صفحه‌ی عمومی و پنل مراجع. تا امروز فقط یک رنگ آزاد
-- (theme_color) قابل انتخاب بود و هیچ تضمینی برای کنتراست خوانا نداشت (مثلا
-- رنگ دیفالت قدیمی «13 148 136» خودش کنتراست کافی روی سفید ندارد). حالا دو
-- روش اضافه می‌شود:
--   preset → یکی از چند رنگ از پیش تاییدشده (کنتراست تضمین‌شده) از grid انتخاب می‌شود
--   logo   → متخصص لوگو/برند خودش را آپلود می‌کند؛ سیستم رنگ غالب را استخراج و
--            در صورت لزوم تیره‌تر می‌کند تا کنتراست کافی روی سفید داشته باشد
-- در هر دو حالت، نتیجه‌ی نهایی همچنان در همان ستون theme_color (تریپلت R G B)
-- ذخیره می‌شود — یعنی همه‌ی جاهایی که از قبل theme_color را می‌خوانند
-- (--brand در globals.css، صفحه‌ی عمومی، پنل مراجع) بدون تغییر کار می‌کنند.
--
-- theme_mode فقط برای این است که پنل تنظیمات بداند کدام تب/grid را به‌عنوان
-- انتخاب فعلی نشان دهد؛ نیازی به ستون جدا برای «کدام preset» نیست چون در حالت
-- preset مقدار theme_color دقیقا برابر یکی از رنگ‌های ثابت grid است.
-- ─────────────────────────────────────────────────────────────────────────────

alter table tenant_profiles
  add column if not exists theme_mode text not null default 'preset' check (theme_mode in ('preset', 'logo')),
  add column if not exists logo_url text;
