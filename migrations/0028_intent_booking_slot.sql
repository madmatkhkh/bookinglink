-- additive — امن روی دیتابیس زنده.
--
-- پرداخت آنلاین: اول انتخاب وقت، بعد پرداخت.
--
-- تا امروز هر دو روش پرداخت یک ترتیب داشتند: پرداخت → تایید → گرفتن وقت. برای
-- کارت‌به‌کارت این درست است و دست نمی‌خورد (تایید دستی دکتر لازم است، پس نمی‌شود
-- وقت را قبل از تایید قطعی کرد). ولی برای پرداخت آنلاین که تاییدش لحظه‌ای است،
-- ترتیب درست برعکس است: مراجع اول وقتش را انتخاب می‌کند، بعد پرداخت می‌کند، و
-- به‌محض تایید پرداخت همان وقت ثبت می‌شود - نه اینکه پول بدهد و بعد ببیند وقت
-- دلخواهش اصلا آزاد نبوده.
--
-- برای همین، وقت انتخابی باید بین «رفتن به درگاه» و «برگشتن از درگاه» جایی
-- بماند: روی خود intent. کال‌بک بعد از verify موفق، این وقت را روی مرحله ثبت
-- می‌کند.
--
-- اگر در این فاصله (چند دقیقه‌ای که مراجع در صفحه‌ی زیبال است) کس دیگری همان
-- ساعت را بگیرد، unique index اسلات (migration 0019) جلوی رزرو دوگانه را
-- می‌گیرد و کال‌بک مرحله را به حالت awaiting_booking می‌برد: پول پرداخت‌شده
-- می‌ماند و مراجع فقط وقت دیگری انتخاب می‌کند. هیچ پولی گم نمی‌شود.
-- ─────────────────────────────────────────────────────────────────────────────

alter table psy_payment_intents
  add column if not exists booking_date text,
  add column if not exists booking_time text;
