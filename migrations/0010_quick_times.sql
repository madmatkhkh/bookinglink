-- ─────────────────────────────────────────────────────────────────────────────
-- Migration 0010 — لیستِ ساعت‌های سریعِ قابلِ‌ویرایشِ هر دکتر
--
-- قبلاً لیستِ ساعت‌های پیشنهادی در تبِ «روزهای کاری» یک ثابتِ هاردکدِ مشترکِ
-- همه‌ی دکترها بود (ALL_TIMES در کد) و هیچ‌جا قابلِ حذف نبود. الان این لیست
-- روی خودِ `psy_resource_profiles` ذخیره می‌شود — یعنی هر دکتر می‌تواند
-- ساعت‌های استانداردِ خودش را اضافه/حذف کند (مثلاً اگر هیچ‌وقت ساعتِ 18 کار
-- نمی‌کند، آن را کامل از گزینه‌ها حذف کند).
--
-- additive است — روی دیتابیسِ زنده هم بدونِ drop قابلِ اجراست. مقدارِ
-- پیش‌فرض همان لیستِ قدیمیِ هاردکد است، پس دکترهای موجود هیچ تغییری نمی‌بینند
-- تا خودشان چیزی را ویرایش کنند.
-- ─────────────────────────────────────────────────────────────────────────────

alter table psy_resource_profiles add column if not exists quick_times jsonb not null default
  '["8:00","9:00","10:00","11:00","12:00","13:00","14:00","15:00","16:00","17:00","18:00"]';
