-- additive — امن روی دیتابیس زنده.
--
-- یکی‌کردن دو مسیر «افزودن جلسه» به یک مسیر.
--
-- تا امروز دکتر برای دادن یک جلسه‌ی تازه به مراجع دو دکمه‌ی جدا داشت:
--   1) «افزودن مرحله‌ی مصاحبه/ارزیابی دیگر» → روی psy_stages می‌نشست، پنل مراجع
--      را باز می‌کرد (پرداخت → گرفتن وقت). فقط دو نوع ثابت: مصاحبه/ارزیابی.
--   2) «ثبت جلسه‌ی تکی جدید» → روی psy_sessions می‌نشست، عنوان دلخواه می‌گرفت،
--      ولی اگر پرونده وارد فاز درمان نشده بود اصلا در پنل مراجع دیده نمی‌شد.
-- نتیجه: هر دو «یک جلسه به مراجع بده» بودند با رفتار متفاوت، و مدام با هم اشتباه
-- گرفته می‌شدند (آن‌قدر که هر دو دکمه به متن هشدار درباره‌ی آن یکی نیاز پیدا
-- کرده بودند - نشانه‌ی روشن اینکه خود مدل غلط بود، نه توضیحش).
--
-- حالا فقط psy_stages می‌ماند (همان مسیری که واقعا مراجع را وارد چرخه‌ی
-- پرداخت/نوبت‌گیری می‌کند) و همان انعطاف عنوان دلخواه مسیر دوم را هم می‌گیرد:
-- stage_type می‌تواند 'custom' باشد و عنوانش در ستون title بنشیند. (ستون
-- stage_type از قبل CHECK نداشت، پس مقدار تازه بدون تغییر constraint می‌نشیند.)
--
-- psy_sessions دست‌نخورده می‌ماند: هم جلسه‌های زیر پروتکل درمان روی همان جدول
-- هستند، هم جلسه‌های تکی قدیمی از قبل ثبت‌شده باید همان‌طور که بودند نمایش
-- داده شوند. فقط «ساختن» جلسه‌ی تکی تازه از پنل حذف می‌شود.
-- ─────────────────────────────────────────────────────────────────────────────

alter table psy_stages
  add column if not exists title text;
