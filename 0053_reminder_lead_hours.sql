-- ───────────────────────────────────────────────────────────────────────────
-- 0053 — فاصله‌ی زمانی یادآوری، قابل تنظیم برای هر مجموعه
-- ───────────────────────────────────────────────────────────────────────────
-- additive و idempotent.
--
-- تا امروز cron یادآوری «نوبت‌های فردا» را پیدا می‌کرد و شب قبل می‌فرستاد —
-- یک رفتار ثابت برای همه. ولی مطب‌ها یکسان نیستند: یکی می‌خواهد شب قبل خبر
-- بدهد، یکی دو ساعت قبل که مراجع یادش نرود راه بیفتد.
--
-- مدل جدید: به‌جای «فردا»، «حداکثر N ساعت مانده به نوبت». cron هر بار
-- نوبت‌هایی را می‌فرستد که فاصله‌شان تا الان از N کمتر شده و هنوز یادآوری
-- نگرفته‌اند (reminder_sent همچنان ضامن عدم تکرار است).
--
-- پیش‌فرض 24 است تا رفتار مجموعه‌های فعلی عملا تغییر نکند.
--
-- ⚠️ نکته‌ی عملیاتی: مقدارهای کوچک (کمتر از 12) فقط وقتی واقعا کار می‌کنند که
-- cron ساعتی اجرا شود. روی cron روزانه، یادآوری «2 ساعت قبل» عملا هیچ‌وقت در
-- پنجره نمی‌افتد. این محدودیت در UI پنل به کاربر گفته می‌شود.
-- ───────────────────────────────────────────────────────────────────────────

alter table public.tenant_profiles
  add column if not exists reminder_lead_hours integer not null default 24;

alter table public.tenant_profiles
  drop constraint if exists tenant_profiles_reminder_lead_check;
alter table public.tenant_profiles
  add constraint tenant_profiles_reminder_lead_check
  check (reminder_lead_hours between 1 and 48);
